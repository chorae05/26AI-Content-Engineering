/*
 * ==============================================================================
 * [윈도우 입력 시스템과 OS I/O 모델]
 * ==============================================================================
 * 1. GetKeyState vs GetAsyncKeyState
 * - GetKeyState: '메시지 큐' 기반. 윈도우 메시지가 처리된 시점의 키 상태를 가져옴. (동기적)
 * - GetAsyncKeyState: 메시지 큐 무시. 호출한 '이 순간' 하드웨어의 실제 상태를 찌름. (비동기적)
 * - 게임 엔진에서는 인풋 렉을 최소화하기 위해 'Async' 버전을 선호함.
 *
 * 2. 왜 0x8000 (1000 0000 0000 0000) 인가?
 * - 리턴값은 16비트 정수(short)임.
 * - 최상위 비트(MSB): 현재 키가 눌려 있는가? (1 = 눌림, 0 = 안눌림)
 * - 최하위 비트(LSB): 지난 호출 이후 키가 한 번이라도 눌렸었는가? (신뢰도 낮음)
 * - 그래서 '& 0x8000' 연산을 통해 "다른 비트는 다 무시하고, '지금 눌림' 상태인지만 보겠다"는 뜻임.
 *
 * 3. 키 상태 판별 로직 (논리 구조)
 * - Key Down (현재 누름): (GetAsyncKeyState(vk) & 0x8000) 이 '참'인 경우.
 * - Key Up (현재 뗌): (GetAsyncKeyState(vk) & 0x8000) 이 '거짓'인 경우.
 * - 0x8001을 안 쓰는 이유: 윈도우 OS 환경에 따라 LSB(0x0001)의 동작이 일관되지 않음.
 *   따라서 순수하게 '지금 이 순간의 물리적 상태'만 체크하려면 0x8000 마스킹이 가장 안전함.
 *
 * 4. 심화: 눌림(Down)과 뗌(Up)의 코드 처리
 * - 단순 체크: if (GetAsyncKeyState(VK_SPACE) & 0x8000) -> 스페이스바 눌림.
 * - 엣지 검출(Edge Detection): 이전 프레임 상태와 비교하여 '방금 눌림' 혹은 '방금 뗌' 구현.
 * - [이전: Up / 현재: Down] -> OnKeyDown (이벤트 발생)
 * - [이전: Down / 현재: Down] -> OnKeyPress (유지 중)
 * - [이전: Down / 현재: Up] -> OnKeyUp (이벤트 발생)
 *
 * --------------------------------------------------------------------------------------------------
 * 상태	        리턴값(16진수)	비트 구조 (MSB...LSB)	비고
 * --------------------------------------------------------------------------------------------------
 * 안 눌림	    0x0000	        0000 0000 0000 0000	    평상시
 * 방금 눌림    0x8001	        1000 0000 0000 0001	    현재 눌려 있고, 이전 호출 이후 변화 있음
 * 누르고 있음	0x8000	        1000 0000 0000 0000	    현재 눌려 있고, 이전 호출에서도 눌려 있었음
 * 방금 뗌	    0x0001	        0000 0000 0000 0001	    현재는 뗐지만, 이전 호출 이후 눌린 적 있음
 *
 * ==============================================================================
 */

#include <windows.h> // Windows 전용 기능(입력, Sleep 등)을 쓰기 위한 도구함
#include <stdio.h>   // printf(출력)를 쓰기 위한 도구함

 // [1] 데이터 설계: 좌표 저장소
typedef struct {    // "구조체(struct)를 정의하겠다"
    float x, y;     // "x, y 좌표를 실수(float)로 저장하겠다" (부드러운 이동을 위해)
} Point;            // "이 구조체의 이름을 'Point'라고 부르겠다"

int main() {
    printf("=== [Input System Deep Dive] ===\n");

    // [2] 초기화: 플레이어의 시작 상태 설정
    Point player = { 10.0f, 10.0f }; // (10, 10) 위치에서 시작 (f는 float 표시)
    int isRunning = 1;               // 게임 루프를 계속 돌릴지 결정하는 스위치 (1: 켬)

    // [3] 게임 루프: 게임이 끝날 때까지 무한 반복 (심장 박동)
    while (isRunning) {

        // --- [입력 처리 단계] ---

        // (A) ESC 키 감시
        short escState = GetAsyncKeyState(VK_ESCAPE); // ESC 키의 '현재 전기 상태'를 가져옴
        if (escState & 0x8000) isRunning = 0;         // 맨 앞 비트(0x8000)가 1이면 스위치를 끔(0)

        // (B) 이동 속도 설정
        float speed = 0.5f; // 한 번의 루프마다 이동할 거리 (작을수록 부드러움)

        // (C) 4방향 키 실시간 체크 (비동기 방식)
        // GetAsyncKeyState('W'): 지금 이 순간 'W'가 물리적으로 눌려 있는지 OS 커널에 물어봄
        // & 0x8000: "눌림 상태" 비트만 골라내어 참/거짓 판별
        if (GetAsyncKeyState('W') & 0x8000) player.y -= speed; // 눌렸으면 y값 감소 (위로 이동)
        if (GetAsyncKeyState('S') & 0x8000) player.y += speed; // 눌렸으면 y값 증가 (아래로 이동)
        if (GetAsyncKeyState('A') & 0x8000) player.x -= speed; // 눌렸으면 x값 감소 (왼쪽 이동)
        if (GetAsyncKeyState('D') & 0x8000) player.x += speed; // 눌렸으면 x값 증가 (오른쪽 이동)

        // --- [출력 처리 단계 (렌더링)] ---

        // (D) 화면 초기화
        system("cls"); // "Clear Screen": 콘솔에 찍힌 이전 글자들을 싹 지움 (잔상 제거)

        // (E) 정보 출력
        printf("Player Info: X:%.1f, Y:%.1f\n", player.x, player.y); // 소수점 첫째자리까지 좌표 출력
        // (unsigned short): 16비트 데이터를 깨끗한 숫자로 보여주기 위한 이름표
        printf("Raw 'A' State: 0x%04X\n", (unsigned short)GetAsyncKeyState('A'));

        // (F) 별(★) 그리기 로직
        // Y좌표만큼 줄바꿈(\n)을 해서 별을 아래로 내림
        for (int i = 0; i < (int)player.y; i++) printf("\n");
        // X좌표만큼 공백(" ")을 띄워서 별을 오른쪽으로 밀어냄
        for (int i = 0; i < (int)player.x; i++) printf(" ");
        printf("★"); // 최종 위치에 플레이어 캐릭터 출력

        // --- [동기화 단계] ---

        // (G) 프레임 조절
        Sleep(30); // 0.03초 동안 CPU를 쉬게 함. (루프 속도를 조절해서 별이 너무 빨리 안 가게 함)
    }

    printf("\nGame Over. 모든 자원을 안전하게 반납했습니다.\n");
    return 0;
}